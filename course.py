def _load_users() -> dict:
    if not os.path.exists(COURSE_FILE):
        return {}
    with open(COURSE_FILE, "r", encoding="utf-8") as f:
        return json.load(f)


def _save_users(users: dict):
    with open(COURSE_FILE, "w", encoding="utf-8") as f:
        json.dump(users, f, ensure_ascii=False, indent=2)


def has_access(user_id: int) -> bool:
    if user_id == 456504792:
        return True
    users = _load_users()
    return str(user_id) in users


def activate_by_username(username: str):
    users = _load_users()
    if username in users:
        return
    users[username] = {
        "day": 0,
        "completed": [],
        "photos_today": [],
        "good_photos": 0,
        "bad_photos": 0,
        "attempts": 0,
        "username": username,
    }
    _save_users(users)


def get_status(user_id: int) -> str | None:
    if not has_access(user_id):
        return None
    users = _load_users()
    uid = str(user_id)
    if uid not in users:
        found = False
        for key, data in users.items():
            if isinstance(data, dict) and data.get("username") == uid:
                found = True
                uid = key
                break
        if not found and user_id == 456504792:
            users["456504792"] = {"day": 0, "completed": [], "photos_today": [], "good_photos": 0, "bad_photos": 0, "attempts": 0, "username": "sevosphoto"}
            _save_users(users)
            return _day_text(0)
        elif not found:
            return None

    day = users[uid].get("day", 1)
    if day == 0:
        return _day_text(0)
    if day > 9:
        return _course_result(uid)
    return _day_text(day)


