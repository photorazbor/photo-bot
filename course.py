def _load_users() -> dict:
    if not os.path.exists(COURSE_FILE):
        return {}
    with open(COURSE_FILE, "r", encoding="utf-8") as f:
        return json.load(f)


def _save_users(users: dict):
    with open(COURSE_FILE, "w", encoding="utf-8") as f:
        json.dump(users, f, ensure_ascii=False, indent=2)


def has_access(user_id: int) -> bool:
    users = _load_users()
    uid = str(user_id)
    return uid in users


def activate_by_username(username: str):
    users = _load_users()
    if username in users:
        return
    users[username] = {"day": 1, "completed": [], "username": username}
    _save_users(users)


def start_course(user_id: int) -> str:
    if not has_access(user_id):
        return "У тебя нет доступа к курсу. Оплати и получи доступ."
    return _day_text(1)


def get_status(user_id: int) -> str | None:
    if not has_access(user_id):
        return None
    users = _load_users()
    uid = str(user_id)
    if uid not in users:
        return None
    day = users[uid]["day"]
    if day > 7:
        return "🎉 Ты прошёл весь мини-курс! Поздравляю!\n\nТы освоил горизонт, правило третей, позу, свет, тень, отражения и фрейминг. Теперь снимай как профи!"
    return _day_text(day)


def complete_day(user_id: int) -> str:
    if not has_access(user_id):
        return "У тебя нет доступа к курсу."
    users = _load_users()
    uid = str(user_id)
    if uid not in users:
        return "У тебя нет доступа к курсу."
    users[uid]["completed"].append(users[uid]["day"])
    users[uid]["day"] += 1
    _save_users(users)
    if users[uid]["day"] > 7:
        return "🎉 Ты прошёл весь мини-курс! Поздравляю!\n\nТы освоил горизонт, правило третей, позу, свет, тень, отражения и фрейминг. Теперь снимай как профи!"
    return f"✅ День {users[uid]['day'] - 1} пройден!\n\n" + _day_text(users[uid]["day"])


def _day_text(day: int) -> str:
    d = DAYS[day]
    return (
        f"📚 <b>День {day}: {d['title']}</b>\n\n"
        f"<b>Теория:</b> {d['theory']}\n\n"
        f"<b>Задание:</b> {d['task']}\n\n"
        f"Пришли фото — я разберу его и мы пойдём дальше!"
    )
